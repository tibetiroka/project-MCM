package dartproductions.mcleodmassacre.options;

/**
 * The supported game quality values
 *
 * @since 0.1.0
 */
public enum QualityOption {
	/**
	 * Indicates bad game quality
	 *
	 * @since 0.1.0
	 */
	LOW,
	/**
	 * Indicates average game quality
	 *
	 * @since 0.1.0
	 */
	NORMAL,
	/**
	 * Indicates good game quality
	 *
	 * @since 0.1.0
	 */
	HIGH
}
